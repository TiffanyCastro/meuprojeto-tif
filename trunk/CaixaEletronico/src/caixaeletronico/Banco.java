
package caixaeletronico;

public class Banco {
    String nomebanco, numerobanco ;
    boolean empimovel, empcarro;

    public String getNomebanco() {
        return nomebanco;
    }

    public void setNomebanco(String nomebanco) {
        this.nomebanco = nomebanco;
    }

    public String getNumerobanco() {
        return numerobanco;
    }

    public void setNumerobanco(String numerobanco) {
        this.numerobanco = numerobanco;
    }

    public boolean getEmpimovel() {
        return empimovel;
    }

    public void setEmpimovel(boolean empimovel) {
        this.empimovel = empimovel;
    }

    public boolean getEmpcarro() {
        return empcarro;
    }

    public void setEmpcarro(boolean empcarro) {
        this.empcarro = empcarro;
    }

    }

